package org.example;

public class Calculate {
    public int add (int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int multiple(int a, int b) {
        return a * b;
    }
    public int devide(int a, int b) {
        return a/b;
    }
}
